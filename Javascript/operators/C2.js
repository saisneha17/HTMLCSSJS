//Assignment Operators (+=, -=, *=, /=, %=, **=)
var a = 10
var b = 20

console.log(a+=1) //11
console.log(a) // 11 (js dynamic changing)

console.log(b-=1) //19
console.log(b) // 19

a++

console.log(a) // 12  (since a++)
console.log(a) // 12

++a

console.log(a) //13
console.log(a++) // 13
console.log(a) // 14

//post increment a++ (after)
//pre increment ++a

var a = 2
var b = 4

console.log(a*=2) // 4
console.log(a) // 4

console.log(b/=2) // 2
console.log(b) // 2

var a = 10
var b = 60

console.log(a%=3) // 1
console.log(a) // 1

console.log(b**=2) //3600
