//charAt()
userName = "NameOne"
console.log(userName.charAt(0)) //N
console.log(userName.charAt(2)) //M
console.log(userName.charAt(12))

//charCodeAt()
product = "Apple"
console.log(product.charCodeAt('0')) // 65
console.log(product.charCodeAt('4')) // 101

//includes
userName = "NameOne"
console.log(userName.includes('N')) //true
console.log(userName.includes('f')) //false

//indexOf()
userName = "E Sneha"
console.log(userName.indexOf('E'))
console.log(userName.indexOf('E', 3))

//lastIndexOf()
userName = "E Swetha"
console.log(userName.lastIndexOf('E'))
console.log(userName.lastIndexOf('E', 3))